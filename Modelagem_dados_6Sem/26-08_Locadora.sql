-- Calcular a média de dias de locação por cliente
SELECT 
    c.id_cliente,
    c.nome,
    AVG(DATEDIFF(COALESCE(l.data_fim, CURDATE()), l.data_inicio)) AS media_dias_locacao
FROM 
    Cliente c
JOIN 
    Locacao l ON c.id_cliente = l.id_cliente
GROUP BY 
    c.id_cliente, c.nome;
