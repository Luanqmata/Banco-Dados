	c.modelo, 
    c.marca, 
    c.ano as Ano_Fabricacao,
    c.cor,
    c.placa,
    c.valor_diaria,
    c.status
FROM Carro c
WHERE status = 'disponível'
  AND id_carro NOT IN (
    SELECT id_carro
    FROM Locacao
    WHERE '2025-08-26' BETWEEN data_inicio AND COALESCE(data_fim, CURDATE())
  );

